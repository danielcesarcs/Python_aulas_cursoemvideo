"""Dobro, triplo e raiz quadrada"""

# Uso de operadores aritméticos 

num = int(input('Digite um número: '))
dob = num * 2  # dobro
tri = num * 3  # triplo
rq = num ** (1/2)  # Raíz quadrada
print('Você digitou {}\ne o seu dobro é {}\no seu triplo é {}\ne sua raiz quadrada é {}'.format(num, dob, tri, rq))
