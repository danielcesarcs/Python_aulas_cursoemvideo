"""Media de 2 valores"""

# Clássica média entre valores
# Uso dos operadores aritméticos e sua ordem de precedência

n1 = int(input('Digite a primeira nota: '))
n2 = int(input('Digite a segunda nota: '))
med = (n1 + n2) / 2
print('A primeirra nota foi {} e segunda foi {};\nO aluno obteve média {}'.format(n1, n2, med))
